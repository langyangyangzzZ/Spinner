package demo.ht.com.spinner;

public class SpinnerBean {

    private String title;
    private int image;

    public SpinnerBean(String title, int image) {
        this.title = title;
        this.image = image;
    }
    public String getTitle() {
        return title;
    }
    public int getImage() {
        return image;
    }
}
